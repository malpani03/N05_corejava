package org.tnsif.WrapperClassDemo;
/*Program to demonstrate on UnBoxing
 * AutoBoxing is nothing but the conversion of object type to primitive type */
public class UnBoxing {

	public static void main(String[] args) {
		Integer num=10;
		
		//Converting the object data type to primitive data type
		int x=num;
		System.out.println(x);
	}

}
